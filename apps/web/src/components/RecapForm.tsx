export function getTemplateById(id: RecapTemplateId): RecapTemplate {
  return recapTemplates.find((template) => template.id === id) ?? recapTemplates[0];
}

export function getTemplatePreview(id: RecapTemplateId): TemplatePreview {
  return templatePreviews[id] ?? templatePreviews.universal;
}

export function getPromptPreview(templateId: RecapTemplateId, savedPrompt: string): string {
  const template = getTemplateById(templateId);
  return `${template.prompt}\n\nCurrent saved prompt:\n${savedPrompt}`;
}

export async function copyPromptText(
  prompt: string,
  clipboard?: Pick<Clipboard, "writeText">
): Promise<"Prompt copied" | "Clipboard unavailable"> {
  if (!clipboard?.writeText) return "Clipboard unavailable";
  await clipboard.writeText(prompt);
  return "Prompt copied";
}

export function updateSection(
  sections: RecapSettings["sections"],
  key: RecapSectionKey,
  patch: Partial<RecapSettings["sections"][number]>
): RecapSettings["sections"] {
  return sections.map((section) => (section.key === key ? { ...section, ...patch } : section));
}

export function moveSection(sections: RecapSettings["sections"], index: number, delta: -1 | 1): RecapSettings["sections"] {
  const nextIndex = index + delta;
  if (nextIndex < 0 || nextIndex >= sections.length) return sections;
  const copy = [...sections];
  const [item] = copy.splice(index, 1);
  copy.splice(nextIndex, 0, item);
  return copy;
}

function sectionLabel(key: RecapSectionKey): string {
  return {
    summary: "Summary",
    decisions: "Decisions",
    actionItems: "Action items",
    openQuestions: "Open questions",
    risks: "Risks",
    followUps: "Follow-ups"
  }[key];
}

function Field({
  label,
  placeholder,
  value,
  onChange
}: {
  label: string;
  placeholder?: string;
  value: string;
  onChange: (value: string) => void;
}) {
  return (
    <label>
      <span>{label}</span>
      <input placeholder={placeholder} value={value} onChange={(event) => onChange(event.target.value)} />
    </label>
  );
}

function TextAreaField({
  label,
  rows,
  value,
  onChange
}: {
  label: string;
  rows: number;
  value: string;
  onChange: (value: string) => void;
}) {
  return (
    <label>
      <span>{label}</span>
      <textarea rows={rows} value={value} onChange={(event) => onChange(event.target.value)} />
    </label>
  );
}
