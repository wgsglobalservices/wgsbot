function buildRecapDepthInstructions(recapDepth: NonNullable<SummaryInput["recapDepth"]>, meetingType: MeetingRecapType): string {
  if (recapDepth === "brief") {
    return [
      "recapDepth is brief.",
      "Generate a simplified short-meeting recap using the resolved meeting type only as context.",
      "Do not use the full standard template structure.",
      "Do not create fake sections like Safety, Sales, Plant, Cost, or Quality unless the short meeting actually discussed them.",
      "meetingNotes must contain exactly 1 heading: Brief meeting recap.",
      "meetingNotes[0].items must contain at most 2 items titled What happened and Outcome.",
      "summary must contain 1 to 3 bullets.",
      "followUpTasks must contain only real tasks supported by the transcript.",
      "decisions must contain only confirmed decisions.",
      "risks should usually be empty unless a real risk was stated.",
      "openQuestions should include transcript limitations if relevant.",
      "If the transcript is mostly empty, joining chatter, bot setup, or low quality, say so clearly instead of inventing notes.",
      "For a short meeting with a real decision or assigned action, keep recapDepth brief and include that decision or action clearly."
    ].join("\n");
  }
  return [
    "recapDepth is standard.",
    "Generate one executive recap with At a Glance first, detailed business-topic context second, then the full action register, open questions, and reference notes.",
    buildMeetingTypeSummaryInstructions(meetingType)
  ].join("\n");
}

