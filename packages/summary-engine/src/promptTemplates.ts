import { meetingRecapTypeLabels, type MeetingRecapType } from "./meetingTypes";
import type { SummaryInput } from "./types";

export function buildSummaryPrompt(input: SummaryInput): string {
  const meetingType = input.meetingType ?? "general";
  const recapDepth = input.recapDepth ?? "standard";
  return [
    buildBaseInstructions(input.prompt),
    `Meeting: ${input.meetingSubject}`,
    input.meetingStartTime ? `Start time: ${input.meetingStartTime}` : "",
    input.meetingEndTime ? `End time: ${input.meetingEndTime}` : "",
    input.meetingDurationMinutes !== undefined ? `Meeting duration minutes: ${input.meetingDurationMinutes}` : "",
    input.transcriptDurationMinutes !== undefined ? `Transcript duration minutes: ${input.transcriptDurationMinutes}` : "",
    input.speakerTurnCount !== undefined ? `Speaker turn count: ${input.speakerTurnCount}` : "",
    input.wordCount !== undefined ? `Word count: ${input.wordCount}` : "",
    input.organizerEmail ? `Organizer: ${input.organizerEmail}` : "",
    `Attendees: ${input.attendees.map((attendee) => attendee.name ? `${attendee.name} <${attendee.email}>` : attendee.email).join(", ")}`,
    `Resolved meeting type: ${meetingType}`,
    `Resolved recap depth: ${recapDepth}`,
    buildRecapDepthInstructions(recapDepth, meetingType),
    "JSON shape: {\"meetingType\":\"weekly_spqrc\"|\"weekly_sales\"|\"plant_meeting\"|\"general\",\"recapDepth\":\"brief\"|\"standard\",\"executiveRecap\":{\"topPriorities\":[{\"title\":\"string\",\"summary\":\"string\",\"whyItMatters\":\"string\",\"owner\":\"string\",\"nextStep\":\"string\",\"dueDate\":\"string\"}],\"immediateActions\":[{\"priority\":\"string\",\"action\":\"string\",\"owner\":\"string\",\"due\":\"string\",\"relatedCustomerOrArea\":\"string\",\"status\":\"string\"}],\"keyDecisions\":[{\"decision\":\"string\",\"impact\":\"string\",\"ownerOrFollowUp\":\"string\"}],\"majorRisks\":[{\"title\":\"string\",\"explanation\":\"string\",\"impact\":\"string\",\"mitigationOrNextStep\":\"string\"}],\"detailedRecap\":[{\"heading\":\"string\",\"summary\":\"string\"}],\"winsAndProgress\":[{\"title\":\"string\",\"detail\":\"string\",\"impact\":\"string\"}],\"fullActionRegister\":[{\"action\":\"string\",\"owner\":\"string\",\"due\":\"string\",\"priority\":\"High\"|\"Medium\"|\"Low\",\"relatedArea\":\"string\",\"notes\":\"string\"}],\"openQuestions\":[{\"question\":\"string\",\"whyItMatters\":\"string\",\"ownerOrBestNextStep\":\"string\"}],\"referenceNotes\":[{\"topic\":\"string\",\"notes\":[\"string\"]}]},\"meetingNotes\":[{\"heading\":\"string\",\"overview\":\"string\",\"items\":[{\"title\":\"string\",\"detail\":\"string\"}]}],\"followUpTasks\":[{\"title\":\"string\",\"description\":\"string\",\"owners\":[\"string\"],\"dueDate\":\"string\"}],\"summary\":string[],\"decisions\":string[],\"actionItems\":[{\"owner\":\"string\",\"task\":\"string\",\"dueDate\":\"string\"}],\"openQuestions\":string[],\"risks\":string[],\"followUps\":string[]}",
    "Transcript:",
    input.transcriptText
  ]
    .filter(Boolean)
    .join("\n");
}

